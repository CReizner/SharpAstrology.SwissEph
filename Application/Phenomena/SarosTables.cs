    /// <summary>
    /// Looks up the Saros series and member number for a solar eclipse at
    /// <paramref name="tjdUt"/>. Returns (-99 999 999, -99 999 999) when no
    /// match is found, mirroring the C source's sentinel.
    /// </summary>
    internal static (double Series, double Member) LookupSolar(double tjdUt) => Lookup(Solar, tjdUt);

    /// <summary>
    /// Looks up the Saros series and member number for a lunar eclipse at
    /// <paramref name="tjdUt"/>. Returns (-99 999 999, -99 999 999) when no
    /// match is found.
    /// </summary>
    internal static (double Series, double Member) LookupLunar(double tjdUt) => Lookup(Lunar, tjdUt);

    private static (double Series, double Member) Lookup(Entry[] table, double tjdUt)
    {
        // Mirrors the logic at swecl.c#L1130-L1149 (solar) and L3342-L3361 (lunar).
        for (int i = 0; i < table.Length; i++)
        {
            var d = (tjdUt - table[i].TStart) / SarosCycle;
            if (d < 0 && d * SarosCycle > -2) d = 0.0000001;
            if (d < 0) continue;
            int j = (int)d;
            if ((d - j) * SarosCycle < 2)
                return (table[i].SeriesNo, j + 1);
            int k = j + 1;
            if ((k - d) * SarosCycle < 2)
                return (table[i].SeriesNo, k + 1);
        }
        return (-99_999_999, -99_999_999);
    }
